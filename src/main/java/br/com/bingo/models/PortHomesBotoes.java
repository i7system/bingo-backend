 

package br.com.bingo.models;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "port_homes_botoes")
public class PortHomesBotoes {
	

	@Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)	
	@Column(name = "IDBOTAO", nullable = false)	
	private int IDBOTAO;
	@Column(name = "ESTILO", length = 250, nullable = false)	
	private String ESTILO;
	@Column(name = "IDACAO", nullable = false)	
	private int IDACAO;
	@Column(name = "LISTA", length = 250, nullable = false)	
	private String LISTA;
	@Column(name = "NOMEBOTAO", length = 50, nullable = false)	
	private String NOMEBOTAO;
	@Column(name = "URL", length = 250, nullable = false)	
	private String URL;
	@Column(name = "IDHOME", nullable = false)	
	private int IDHOME;	
	@Column(name = "STATUS", nullable = false)	
	private int STATUS;
	public int getIDBOTAO() {
		return IDBOTAO;
	}
	public void setIDBOTAO(int iDBOTAO) {
		IDBOTAO = iDBOTAO;
	}
	public String getESTILO() {
		return ESTILO;
	}
	public void setESTILO(String eSTILO) {
		ESTILO = eSTILO;
	}
	public String getLISTA() {
		return LISTA;
	}
	public void setLISTA(String lISTA) {
		LISTA = lISTA;
	}
	public String getNOMEBOTAO() {
		return NOMEBOTAO;
	}
	public void setNOMEBOTAO(String nOMEBOTAO) {
		NOMEBOTAO = nOMEBOTAO;
	}
	public String getURL() {
		return URL;
	}
	public void setURL(String uRL) {
		URL = uRL;
	}
	public int getIDACAO() {
		return IDACAO;
	}
	public void setIDACAO(int iDACAO) {
		IDACAO = iDACAO;
	}
	public int getIDHOME() {
		return IDHOME;
	}
	public void setIDHOME(int iDHOME) {
		IDHOME = iDHOME;
	}
	public int getSTATUS() {
		return STATUS;
	}
	public void setSTATUS(int sTATUS) {
		STATUS = sTATUS;
	}


	
}
	

