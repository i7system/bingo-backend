package br.com.bingo.models;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="sec_menus_perfis")
public class SecMenusPerfis {
	
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name = "IDMENUPERFIL", nullable = false)	
	private int IDMENUPERFIL;
	@Column(name = "IDEMPRESA",nullable = false)	
	private int IDEMPRESA;
	@Column(name = "IDMENU",nullable = false)	
	private int IDMENU;
	@Column(name = "IDPERFIL",nullable = false)	
	private int IDPERFIL;
	
	public int getIDMENUPERFIL() {
		return IDMENUPERFIL;
	}
	public void setIDMENUPERFIL(int iDMENUPERFIL) {
		IDMENUPERFIL = iDMENUPERFIL;
	}
	public int getIDEMPRESA() {
		return IDEMPRESA;
	}
	public void setIDEMPRESA(int iDEMPRESA) {
		IDEMPRESA = iDEMPRESA;
	}
	public int getIDMENU() {
		return IDMENU;
	}
	public void setIDMENU(int iDMENU) {
		IDMENU = iDMENU;
	}
	public int getIDPERFIL() {
		return IDPERFIL;
	}
	public void setIDPERFIL(int iDPERFIL) {
		IDPERFIL = iDPERFIL;
	}

	
	

}
